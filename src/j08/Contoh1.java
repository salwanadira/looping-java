public class Contoh1 {
    public static void main(String[] args)
    {
        //PERULANGAN FOR
        int x;
        for(x = 1; x<= 5; x++)
        {
            System.out.println("SMK Telkom Malang");
        }
        
        //PERULANGAN WHILE
        int i = 1;
        while(1 <= 5)
        {
            System.out.println("SMK Telkom Malang");
            i++;
        }
        
        //PERULANGAN DO WHILE
        int j = 1;
        do
        { 
            System.out.println("SMK Telkom Malang");
            j++;
        }
        while(j <= 5);
    }
}
